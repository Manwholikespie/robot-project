function properIndex = marbleNumToIndex(marbleNum)
    switch (marbleNum)
        case (5)
            properIndex = 3;
        case (6)
            properIndex = 2;
        case (3)
            properIndex = 6;
        case (4)
            properIndex = 5;
        case (1)
            properIndex = 9;
        case (2)
            properIndex = 8;
        case (11)
            properIndex = 12;
        case (12)
            properIndex = 11;
    end
end